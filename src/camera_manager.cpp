// Thread-safe camera count access
size_t CameraManager::getLoadedCount() const {
  if (cameraMutex && xSemaphoreTake(cameraMutex, pdMS_TO_TICKS(10)) == pdTRUE) {
    size_t count = cameras.size();
    xSemaphoreGive(cameraMutex);
    return count;
  }
  return cameras.size();  // Fall back to unsynchronized if mutex fails
}

// Static task entry point - calls instance method
void CameraManager::loadTaskEntry(void* param) {
  CameraManager* self = static_cast<CameraManager*>(param);
  
  Serial.println("[Camera] Background load task started");
  
  // Perform the incremental database load
  bool success = self->loadDatabaseIncremental();
  
  // Mark loading complete
  self->backgroundLoading = false;
  self->loadProgressPercent = success ? 100 : 0;
  
  Serial.printf("[Camera] Background load task complete: %s (%d cameras)\n",
                success ? "success" : "failed", self->cameras.size());
  
  // Delete the task (task deletes itself when done)
  self->loadTaskHandle = nullptr;
  vTaskDelete(NULL);
}

// Start background loading - returns immediately
bool CameraManager::startBackgroundLoad() {
  if (!fs) {
    Serial.println("[Camera] Cannot start background load - no filesystem");
    return false;
  }
  
  if (backgroundLoading) {
    Serial.println("[Camera] Background load already in progress");
    return false;
  }
  
  // Mark loading as in progress
  backgroundLoading = true;
  loadProgressPercent = 0;
  loadTaskShouldExit = false;
  
  // Create background task with low priority (1)
  // Lower than BLE (5) and display (2) to avoid interfering with UI
  // Stack size increased to 8192 for binary batch buffer (64 * 24 = 1536 bytes)
  BaseType_t result = xTaskCreate(
    loadTaskEntry,          // Task function
    "CamLoad",              // Task name
    8192,                   // Stack size (bytes) - increased for binary loading
    this,                   // Parameter (this pointer)
    1,                      // Priority (low)
    &loadTaskHandle         // Task handle
  );
  
  if (result != pdPASS) {
    Serial.println("[Camera] Failed to create background load task");
    backgroundLoading = false;
    return false;
  }
  
  Serial.println("[Camera] Background load task created");
  return true;
}

// Stop background loading (if in progress)
void CameraManager::stopBackgroundLoad() {
  if (!backgroundLoading || !loadTaskHandle) {
    return;
  }
  
  Serial.println("[Camera] Stopping background load...");
  loadTaskShouldExit = true;
  
  // Wait for task to exit (up to 2 seconds)
  for (int i = 0; i < 20 && loadTaskHandle; i++) {
    vTaskDelay(pdMS_TO_TICKS(100));
  }
  
  // Force delete if still running
  if (loadTaskHandle) {
    vTaskDelete(loadTaskHandle);
    loadTaskHandle = nullptr;
    Serial.println("[Camera] Background load task forcefully terminated");
  }
  
  backgroundLoading = false;
}

// Incremental database load with yielding - called from background task
bool CameraManager::loadDatabaseIncremental() {
  // List of database files to load (in order)
  // Check for binary files first, then fall back to JSON
  struct DatabaseFile {
    const char* binPath;
    const char* jsonPath;
    bool useBinary;
    bool exists;
    size_t approxRecords;  // Estimate for progress calculation
  };
  
  DatabaseFile files[] = {
    {"/alpr.bin", "/alpr.json", false, false, 70000},
    {"/redlight_cam.bin", "/redlight_cam.json", false, false, 200},
    {"/speed_cam.bin", "/speed_cam.json", false, false, 1200}
  };
  const int numFiles = sizeof(files) / sizeof(files[0]);
  
  // Check which files exist (prefer binary)
  size_t totalRecords = 0;
  int existingFiles = 0;
  for (int i = 0; i < numFiles; i++) {
    if (fs->exists(files[i].binPath)) {
      files[i].exists = true;
      files[i].useBinary = true;
      totalRecords += files[i].approxRecords;
      existingFiles++;
    } else if (fs->exists(files[i].jsonPath)) {
      files[i].exists = true;
      files[i].useBinary = false;
      totalRecords += files[i].approxRecords;
      existingFiles++;
    }
  }
  
  if (existingFiles == 0) {
    Serial.println("[Camera] No database files found for background load");
    return false;
  }
  
  Serial.printf("[Camera] Background loading %d files (~%d records)...\n", 
                existingFiles, totalRecords);
  
  uint32_t overallStart = millis();
  size_t recordsLoaded = 0;
  bool firstFile = true;
  
  for (int fileIdx = 0; fileIdx < numFiles; fileIdx++) {
    if (loadTaskShouldExit) {
      Serial.println("[Camera] Background load cancelled");
      return false;
    }
    
    if (!files[fileIdx].exists) continue;
    
    const char* path = files[fileIdx].useBinary ? 
                       files[fileIdx].binPath : files[fileIdx].jsonPath;
    
    // Clear cameras only for first file
    if (firstFile) {
      if (cameraMutex && xSemaphoreTake(cameraMutex, portMAX_DELAY) == pdTRUE) {
        cameras.clear();
        cameras.reserve(totalRecords + 1000);
        xSemaphoreGive(cameraMutex);
      }
      firstFile = false;
    }
    
    uint32_t fileStart = millis();
    size_t fileRecords = 0;
    
    if (files[fileIdx].useBinary) {
      // Fast binary load
      fileRecords = loadBinaryDatabaseIncremental(path);
    } else {
      // Slow JSON load with yielding
      fileRecords = loadJsonDatabaseIncremental(path);
    }
    
    recordsLoaded += fileRecords;
    
    // Update progress
    loadProgressPercent = (totalRecords > 0) ? 
                          (recordsLoaded * 100 / totalRecords) : 0;
    
    uint32_t fileElapsed = millis() - fileStart;
    Serial.printf("[Camera] Loaded %zu from %s in %lums (bg%s)\n", 
                  fileRecords, path, static_cast<unsigned long>(fileElapsed),
                  files[fileIdx].useBinary ? ",bin" : "");
  }
  
  // Build spatial index after all files loaded
  buildSpatialIndex();
  
  loadProgressPercent = 100;
  
  uint32_t totalElapsed = millis() - overallStart;
  Serial.printf("[Camera] Background load complete: %zu cameras in %lums\n",
                cameras.size(), static_cast<unsigned long>(totalElapsed));
  
  return !cameras.empty();
}

// Load binary file incrementally (with task yielding)
size_t CameraManager::loadBinaryDatabaseIncremental(const char* path) {
  File file = fs->open(path, "r");
  if (!file) {
    Serial.printf("[Camera] FAIL open %s\n", path);
    return 0;
  }
  
  // Read header manually to avoid alignment issues on ESP32
  // Binary format: VCAM (4) + version (4, uint32 LE) + count (4, LE) + recordSize (4, LE) = 16 bytes
  uint8_t headerBuf[16];
  size_t bytesRead = file.read(headerBuf, 16);
  
  if (bytesRead != 16) {
    Serial.printf("[Camera] FAIL header %s\n", path);
    file.close();
    return 0;
  }
  
  // Parse header fields manually (little-endian, all 4-byte fields after magic)
  char magic[5] = {(char)headerBuf[0], (char)headerBuf[1], (char)headerBuf[2], (char)headerBuf[3], 0};
  uint32_t version = headerBuf[4] | (headerBuf[5] << 8) | (headerBuf[6] << 16) | (headerBuf[7] << 24);
  uint32_t count = headerBuf[8] | (headerBuf[9] << 8) | (headerBuf[10] << 16) | (headerBuf[11] << 24);
  uint32_t recordSize = headerBuf[12] | (headerBuf[13] << 8) | (headerBuf[14] << 16) | (headerBuf[15] << 24);
  
  if (memcmp(magic, "VCAM", 4) != 0) {
    Serial.printf("[Camera] Bad magic %s\n", path);
    file.close();
    return 0;
  }
  
  if (version != 1) {
    Serial.printf("[Camera] Bad version %s\n", path);
    file.close();
    return 0;
  }
  
  // Handle legacy files that wrote recordSize=0
  if (recordSize == 0) {
    recordSize = sizeof(BinaryRecord);  // Default to 24
  }
  
  if (recordSize != sizeof(BinaryRecord)) {
    Serial.printf("[Camera] Bad recSize %s\n", path);
    file.close();
    return 0;
  }
  
  // Read records in batches (128 records × 24 bytes = 3KB stack, safe for ESP32-S3)
  const size_t BATCH_SIZE = 128;
  BinaryRecord batch[BATCH_SIZE];
  size_t remaining = count;
  size_t loaded = 0;
  
  while (remaining > 0 && !loadTaskShouldExit) {
    size_t toRead = (remaining > BATCH_SIZE) ? BATCH_SIZE : remaining;
    size_t bytesRead = file.read((uint8_t*)batch, toRead * sizeof(BinaryRecord));
    
    if (bytesRead != toRead * sizeof(BinaryRecord)) {
      Serial.println("[Camera] Unexpected end of binary file");
      break;
    }
    
    // Add to camera vector (thread-safe)
    if (cameraMutex && xSemaphoreTake(cameraMutex, pdMS_TO_TICKS(50)) == pdTRUE) {
      for (size_t i = 0; i < toRead; i++) {
        CameraRecord record;
        record.latitude = batch[i].lat;
        record.longitude = batch[i].lon;
        record.snapLat = batch[i].snapLat;
        record.snapLon = batch[i].snapLon;
        record.roadBearing = batch[i].bearing / 10.0f;
        record.corridorWidthM = batch[i].width;
        record.bearingTolerance = batch[i].tolerance;
        record.type = batch[i].type;
        record.speedLimit = batch[i].speedLimit;
        record.isMetric = (batch[i].flags & 0x01) != 0;
        cameras.push_back(record);
      }
      xSemaphoreGive(cameraMutex);
    }
    
    loaded += toRead;
    remaining -= toRead;
    
    // Yield every batch to allow other tasks to run
    vTaskDelay(pdMS_TO_TICKS(1));
  }
  
  file.close();
  return loaded;
}

// Load JSON file incrementally (with task yielding) - slow path
size_t CameraManager::loadJsonDatabaseIncremental(const char* path) {
  File file = fs->open(path, "r");
  if (!file) {
    Serial.printf("[Camera] Failed to open %s\n", path);
    return 0;
  }
  
  size_t fileRecords = 0;
  size_t linesProcessed = 0;
  char lineBuffer[256];
  size_t bufIdx = 0;
  
  while (file.available()) {
    // Check for cancellation and yield every 100 lines
    if ((linesProcessed % 100) == 0) {
      if (loadTaskShouldExit) {
        file.close();
        return fileRecords;
      }
      vTaskDelay(pdMS_TO_TICKS(1));
    }
    
    char c = file.read();
    
    if (c == '\n' || c == '\r') {
      if (bufIdx > 0) {
        lineBuffer[bufIdx] = '\0';
        
        CameraRecord record;
        if (parseCameraLine(lineBuffer, record)) {
          if (cameraMutex && xSemaphoreTake(cameraMutex, pdMS_TO_TICKS(50)) == pdTRUE) {
            cameras.push_back(record);
            xSemaphoreGive(cameraMutex);
            fileRecords++;
          }
        }
        
        linesProcessed++;
        bufIdx = 0;
      }
    } else if (bufIdx < sizeof(lineBuffer) - 1) {
      lineBuffer[bufIdx++] = c;
    }
  }
  
  // Handle last line without newline
  if (bufIdx > 0) {
    lineBuffer[bufIdx] = '\0';
    CameraRecord record;
    if (parseCameraLine(lineBuffer, record)) {
      if (cameraMutex && xSemaphoreTake(cameraMutex, pdMS_TO_TICKS(50)) == pdTRUE) {
        cameras.push_back(record);
        xSemaphoreGive(cameraMutex);
        fileRecords++;
      }
    }
  }
  
  file.close();
  return fileRecords;
}
// ============================================================================
// Incremental Cache Building - Non-blocking cache construction
// ============================================================================

bool CameraManager::startIncrementalCacheBuild(float lat, float lon, float radiusMiles) {
  // Don't start if already in progress
  if (incrementalBuild.inProgress) {
    Serial.println("[Camera] Incremental build already in progress");
    return false;
  }
  
  // Convert radius to meters and calculate lat/lon deltas for quick filtering
  float radiusM = radiusMiles * 1609.34f;
  float latDelta = radiusM / 111320.0f;  // ~111km per degree latitude
  float lonDelta = radiusM / (111320.0f * cosf(lat * DEG_TO_RAD));
  
  // Initialize incremental state
  incrementalBuild.inProgress = true;
  incrementalBuild.currentIndex = 0;
  incrementalBuild.targetLat = lat;
  incrementalBuild.targetLon = lon;
  incrementalBuild.radiusM = radiusM;
  incrementalBuild.latDelta = latDelta;
  incrementalBuild.lonDelta = lonDelta;
  incrementalBuild.pendingCache.clear();
  incrementalBuild.pendingCache.reserve(500);  // Reserve reasonable space
  incrementalBuild.startTimeMs = millis();
  incrementalBuild.radiusMi = radiusMiles;  // Store for finish
  
  Serial.printf("[Camera] Starting incremental cache build: %.6f, %.6f, %.0f mi\n",
                lat, lon, radiusMiles);
  
  return true;
}

bool CameraManager::continueIncrementalCacheBuild(size_t maxCamerasPerCall) {
  if (!incrementalBuild.inProgress) {
    return true;  // Nothing to do, consider it complete
  }
  
  // Brief mutex acquisition to read camera data
  if (!cameraMutex || xSemaphoreTake(cameraMutex, pdMS_TO_TICKS(10)) != pdTRUE) {
    return false;  // Couldn't get mutex, try again later
  }
  
  size_t totalCameras = cameras.size();
  size_t processed = 0;
  
  while (incrementalBuild.currentIndex < totalCameras && processed < maxCamerasPerCall) {
    const CameraRecord& cam = cameras[incrementalBuild.currentIndex];
    
    // Quick bounding box check first (very fast)
    float latDiff = fabsf(cam.latitude - incrementalBuild.targetLat);
    float lonDiff = fabsf(cam.longitude - incrementalBuild.targetLon);
    
    if (latDiff <= incrementalBuild.latDelta && lonDiff <= incrementalBuild.lonDelta) {
      // Within bounding box, do precise distance check
      float dist = haversineDistance(cam.latitude, cam.longitude,
                                     incrementalBuild.targetLat, incrementalBuild.targetLon);
      if (dist <= incrementalBuild.radiusM) {
        incrementalBuild.pendingCache.push_back(cam);
      }
    }
    
    incrementalBuild.currentIndex++;
    processed++;
  }
  
  xSemaphoreGive(cameraMutex);
  
  // Check if complete
  if (incrementalBuild.currentIndex >= totalCameras) {
    return true;  // Build complete
  }
  
  return false;  // More work to do
}

bool CameraManager::finishIncrementalCacheBuild() {
  if (!incrementalBuild.inProgress) {
    return false;
  }
  
  // Brief mutex to swap the cache
  if (cameraMutex && xSemaphoreTake(cameraMutex, pdMS_TO_TICKS(50)) == pdTRUE) {
    // Move the pending cache to the active cache
    regionalCache = std::move(incrementalBuild.pendingCache);
    cacheCenterLat = incrementalBuild.targetLat;
    cacheCenterLon = incrementalBuild.targetLon;
    cacheRadiusMi = incrementalBuild.radiusMi;
    xSemaphoreGive(cameraMutex);
  }
  
  uint32_t elapsed = millis() - incrementalBuild.startTimeMs;
  Serial.printf("[Camera] Incremental cache complete: %zu cameras in %lu ms (non-blocking)\n",
                regionalCache.size(), static_cast<unsigned long>(elapsed));
  
  // Reset state
  incrementalBuild.inProgress = false;
  incrementalBuild.pendingCache.clear();
  incrementalBuild.pendingCache.shrink_to_fit();  // Release memory
  
  return true;
}
